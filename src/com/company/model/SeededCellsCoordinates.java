package com.company.model;

/**
 * Class for seeded cells coordinates set up.
 */
public final class SeededCellsCoordinates {
    private final int x;
    private final int y;

    public SeededCellsCoordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
